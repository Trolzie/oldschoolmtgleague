<h3>Chaos Orb</h3>
<p>Casting cost: 2</p>
<p>Type: Artifact</p>
<p>1, Tap: Choose a nontoken permanent on the battlefield. If Chaos Orb is on the battlefield, flip Chaos Orb onto the battlefield from a height of at least one foot. If Chaos Orb turns over completely at least once during the flip, and lands resting on the chosen permanent, destroy that permanent. Then destroy Chaos Orb.
(Note: because of how Chaos Orb is worded, with it being destroyed after a flip, it can still be Disenchanted or Shattered in response to the activation. This is consistent with the wording of Chaos Orb not being sacrificed upon activation, as it probably would with modern templating.)</p>

