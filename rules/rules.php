<h3>Additional Rules</h3>
<p>Proxies are a great way to help complete your deck and play with it at maximum capacity. We allow up to 20 proxies in each deck. You must however show your opponent beforehand so he/she can distinguish them properly. Moreover, the proxy must be legible to your opponent during all game states.</p>
<p>As mentioned under the legal sets section, using cards which share art with the original is allowed. Although most of us probably strive towards a "true" 93/94 deck, we are also aware that prices on said cards can be steep. We don't believe this should get in the way of encouraging and playing Magic with players around the world, which is why we allow all versions that share art with the original. Below is an example of the card "City of Brass" to illustrate this point.</p>
<img src="../assets/cityofbrassartlegality.jpg">
<p class="image-tag">The first card is from "Arabian nights" and is the original printing. The second is a different language, and the third from the set "Chronicles". Both are still legal since they have the same image. The last two cards are from "5th" and "8th" edition, but are not legal, since they do not share art with the original printing.</p>
